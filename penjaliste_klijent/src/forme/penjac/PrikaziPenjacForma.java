    public void prepareView() {
        popuniTabelu();
        
        try {
            jComboBoxKategorija.removeAllItems();
            List<Kategorija> kategorije = ControllerClient.getInstance().vratiKategorije();
            for (Kategorija k : kategorije) {
                jComboBoxKategorija.addItem(k);
            }
            jComboBoxKategorija.setSelectedIndex(-1);
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void popuniTabelu() {
        try {
            List<Penjac> penjaci = ControllerClient.getInstance().vratiPenjace();
            ModelTabelePenjac mtp = new ModelTabelePenjac(penjaci);
            jTablePenjaci.setModel(mtp);
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }
