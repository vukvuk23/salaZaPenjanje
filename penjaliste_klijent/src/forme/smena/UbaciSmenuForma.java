    private void jButtonUbaciActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUbaciActionPerformed
        try {
            String naziv = jTextFieldNaziv.getText().trim();
            String vremeOdString = jTextFieldVremeOd.getText().trim();
            String vremeDoString = jTextFieldVremeDo.getText().trim();

            validiraj(naziv, vremeOdString, vremeDoString);

            LocalTime vremeOd = LocalTime.parse(vremeOdString);
            LocalTime vremeDo = LocalTime.parse(vremeDoString);

            Smena smena = new Smena(null, naziv, vremeOd, vremeDo);
            ControllerClient.getInstance().ubaciSmenu(smena);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio smenu!", "Uspeh!", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonUbaciActionPerformed
