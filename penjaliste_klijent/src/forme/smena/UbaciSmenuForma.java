    private void validiraj(String naziv, String vremeOd, String vremeDo) throws Exception {
        String error = "";
    
        if (naziv.isEmpty()) {
            error += "Naziv smene ne moze biti prazan!\n";
        }
        if (vremeOd.isEmpty()) {
            error += "Vreme pocetka ne moze biti prazno!\n";
        } else {
            try {
                LocalTime.parse(vremeOd);
            } catch (Exception e) {
                error += "Vreme pocetka mora biti u formatu HH:mm!\n";
            }
        }
        if (vremeDo.isEmpty()) {
            error += "Vreme kraja ne moze biti prazno!\n";
        } else {
            try {
                LocalTime.parse(vremeDo);
            } catch (Exception e) {
                error += "Vreme kraja mora biti u formatu HH:mm!\n";
            }
        }

        if (!error.isEmpty()) {
            throw new Exception(error);
        }
    }
