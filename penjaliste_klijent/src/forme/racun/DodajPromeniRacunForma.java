    private void jButtonDodajStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajStavkuActionPerformed
        Usluga usluga = (Usluga) jComboBoxUsluge.getSelectedItem();
        String kolicina = jTextFieldKolicina.getText().trim();
        
        try {
            validirajStavku(usluga, kolicina);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        StavkaRacuna sr = new StavkaRacuna(r, Integer.parseInt(kolicina), usluga);
        stavkeRacuna.add(sr);
        
        popuniTabeluStavki();
        jComboBoxUsluge.setSelectedIndex(-1);
        jTextFieldKolicina.setText("");  
    }//GEN-LAST:event_jButtonDodajStavkuActionPerformed

    private void jButtonObrisiStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrisiStavkuActionPerformed
        int red = jTableStavkeRacuna.getSelectedRow();
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Nijedna stavka nije odabrana!", "Greska!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        stavkeRacuna.remove(red);
        popuniTabeluStavki();
    }//GEN-LAST:event_jButtonObrisiStavkuActionPerformed

    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
        try {
            Zaposleni z = (Zaposleni) jComboBoxZaposleni.getSelectedItem();
            Penjac p = (Penjac) jComboBoxPenjac.getSelectedItem();

            validirajRacun(z, p);

            

            if (r == null) {
                Racun racun = new Racun();
                racun.setZaposleni(z);
                racun.setPenjac(p);
                racun.setDatumVreme(LocalDateTime.now());
                racun.setStavkeRacuna(stavkeRacuna);
                racun.izracunajUkupanIznos();
                ControllerClient.getInstance().kreirajRacun(racun);
                JOptionPane.showMessageDialog(this, "Sistem je zapamtio racun!", "Uspeh!", JOptionPane.INFORMATION_MESSAGE);
            } else {
                r.setZaposleni(z);
                r.setPenjac(p);
                r.setDatumVreme(LocalDateTime.now());
                r.setStavkeRacuna(stavkeRacuna);
                r.izracunajUkupanIznos();
                ControllerClient.getInstance().izmeniRacun(r);
                JOptionPane.showMessageDialog(this, "Sistem je promenio racun!", "Uspeh!", JOptionPane.INFORMATION_MESSAGE);
            }

            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSacuvajActionPerformed

