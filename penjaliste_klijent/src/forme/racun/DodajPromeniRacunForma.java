    private void prepareView() {
        try {
            jComboBoxZaposleni.removeAllItems();
            List<Zaposleni> zaposleni = ControllerClient.getInstance().vratiZaposlene();
            for (Zaposleni z : zaposleni) {
                jComboBoxZaposleni.addItem(z);
            }
            jComboBoxZaposleni.setSelectedIndex(-1);

            jComboBoxPenjac.removeAllItems();
            List<Penjac> penjaci = ControllerClient.getInstance().vratiPenjace();
            for (Penjac p : penjaci) {
                jComboBoxPenjac.addItem(p);
            }
            jComboBoxPenjac.setSelectedIndex(-1);
            
            jComboBoxUsluge.removeAllItems();
            List<Usluga> usluge = ControllerClient.getInstance().vratiUsluge();
            for (Usluga u : usluge) {
                jComboBoxUsluge.addItem(u);
            }
            jComboBoxUsluge.setSelectedIndex(-1);
            
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd.MM.yyyy HH:mm");
            jTextFieldDatumIVreme.setText(LocalDateTime.now().format(formatter));
            
            jTextFieldUkupanIznos.setEditable(false);
            jTextFieldDatumIVreme.setEditable(false);
            popuniTabeluStavki();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void popuniPolja() {
        jComboBoxZaposleni.setSelectedItem(r.getZaposleni());
        jComboBoxPenjac.setSelectedItem(r.getPenjac());

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd.MM.yyyy HH:mm");
        jTextFieldDatumIVreme.setText(r.getDatumVreme().format(formatter));

        jTextFieldUkupanIznos.setText(String.valueOf(r.getUkupanIznos()));
        
        popuniTabeluStavki();
        jTextFieldDatumIVreme.setEditable(true);
    }

    private void popuniTabeluStavki() {
        ModelTabeleStavkaRacuna mts = new ModelTabeleStavkaRacuna(stavkeRacuna);
        jTableStavkeRacuna.setModel(mts);
        
        Racun racun = new Racun();
        racun.setStavkeRacuna(stavkeRacuna);
        racun.izracunajUkupanIznos();
        jTextFieldUkupanIznos.setText(String.valueOf(racun.getUkupanIznos()));
    }

    private void validirajStavku(Usluga usluga, String kolicina) throws Exception {
        String error = "";

        if (usluga == null) {
            error += "Nijedna usluga nije odabrana!\n";
        }

        if (kolicina.isEmpty()) {
            error += "Polje kolicina ne moze biti prazno!\n";
        } else {
            try {
                int kol = Integer.parseInt(kolicina);
                if (kol <= 0) {
                    error += "Kolicina mora biti pozitivan broj!\n";
                }
            } catch (NumberFormatException e) {
                error += "Kolicina mora biti ceo broj!\n";
            }
        }

        if (!error.isEmpty()) {
            throw new Exception(error);
        }
    }

    private void validirajRacun(Zaposleni z, Penjac p) throws Exception {
        String error = "";
    
        if (z == null) {
            error += "Morate odabrati zaposlenog!\n";
        }
        if (p == null) {
            error += "Morate odabrati penjaca!\n";
        }
        if (stavkeRacuna.isEmpty()) {
            error += "Racun mora imati bar jednu stavku!\n";
        }

        if (!error.isEmpty()) {
            throw new Exception(error);
        }
    }
